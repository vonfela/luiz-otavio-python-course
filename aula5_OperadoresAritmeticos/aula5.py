"""
operadores aritméticos:

+ - adição
- - subtração
* - multiplicação
/ - divisão
// - divisão inteira (arredondamento)
** - exponenciação/potenciação, um número elevado a outro
% - retorna um módulo, o resto da divisão entre um número e outro
() - Alterar as precedências nas contas


"""

print(10 * 10)
print(10 + 10)
print(10 - 10)
print(10 / 10)
print('felipe' * 10) #repetição
print('felipe' + 'cavalcanti') #concatenação
print('Felipe tem' + ' ' + str(27) + 'anos de idade') #conversão de inteiro pra string
print(10 / 3)
print(10 // 3)
print(2 ** 10)
print(10 % 5)
print(10 % 3)
print(5+2*10)
print((5+2)*10) #alterar precedência


